# Užitočné klávesové skratky pre Visual Studio Code

## Základné pohyby
#- **Prejsť na začiatok riadka**: `Home`
#- **Prejsť na koniec riadka**: `End`
#- **Prejsť na začiatok dokumentu**: `Ctrl + Home`
#- **Prejsť na koniec dokumentu**: `Ctrl + End`
#- **Preskočiť slovo vľavo/vpravo**: `Ctrl + ← / Ctrl + →`

## Označovanie
#- **Označiť celý riadok**: `Ctrl + L`
#- **Označiť všetko**: `Ctrl + A`
#- **Označiť od kurzora po začiatok riadka**: `Shift + Home`
#- **Označiť od kurzora po koniec riadka**: `Shift + End`
#- **Označiť slovo**: `Ctrl + Shift + → / Ctrl + Shift + ←`

## Editácia
#- **Kopírovať riadok hore/dole**: `Shift + Alt + ↑ / Shift + Alt + ↓`
#- **Presunúť riadok hore/dole**: `Alt + ↑ / Alt + ↓`
#- **Vymazať celý riadok**: `Ctrl + Shift + K`
#- **Zalomiť riadok (viac riadkov do jedného)**: `Ctrl + Shift + P` → `Join Lines`
#- **Formátovať dokument**: `Shift + Alt + F`

## Viacnásobné kurzory
#- **Vytvoriť ďalší kurzor hore/dole**: `Ctrl + Alt + ↑ / Ctrl + Alt + ↓`
#- **Označiť ďalší výskyt slova**: `Ctrl + D`
#- **Označiť všetky výskyty slova**: `Ctrl + Shift + L`

## Vyhľadávanie a navigácia
#- **Vyhľadávať v súbore**: `Ctrl + F`
#- **Nahradiť vo súbore**: `Ctrl + H`
#- **Prejsť na riadok**: `Ctrl + G`
#- **Prejsť na symbol v súbore (funkcia, trieda...)**: `Ctrl + Shift + O`
#- **Otvoriť súbor**: `Ctrl + P`

## Terminál
#- **Otvoriť/zatvoriť terminál**: `Ctrl + ` (backtick)

